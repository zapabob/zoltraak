from quantum_circuit_library import 量子回路, シミュレーター

# 2量子ビットの量子回路を作成
回路 = 量子回路(量子ビット数=2)

# 量子ゲートを適用
回路.𝐇(0)  # 0番目の量子ビットにアダマールゲート(𝐇)を適用
回路.𝐂𝐍𝐎𝐓(0, 1)  # 0番目の量子ビットを制御、1番目をターゲットとしたCNOTゲート(𝐂𝐍𝐎𝐓)を適用

# 測定を行う
測定結果 = 回路.測定()
print("測定結果:", 測定結果)

# シミュレーターを使って量子回路を実行
simulator = シミュレーター()
シミュレーション結果 = simulator.実行(回路)
print("シミュレーション結果:", シミュレーション結果)